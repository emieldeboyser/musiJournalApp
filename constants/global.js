export const formatDate = (date) => {
  const d = new Date(date);
  const minutes = d.getMinutes().toString().padStart(2, "0");
  return `${d.getDate()} ${d.toLocaleString("default", {
    month: "long",
  })} ${d.getFullYear()} at ${d.getHours()}:${minutes}`;
};

