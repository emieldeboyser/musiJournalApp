export async function refreshAccessToken() {
  const refreshToken = await SecureStore.getItemAsync("refresh_token");

  const client_id = process.env.SPOTIFY_CLIENT_ID;
  const client_secret = process.env.SPOTIFY_CLIENT_SECRET;

  let authOptions = {
    url: "https://accounts.spotify.com/api/token",
    headers: {
      "content-type": "application/x-www-form-urlencoded",
      Authorization:
        "Basic " +
        new Buffer.from(client_id + ":" + client_secret).toString("base64"),
    },
    form: {
      grant_type: "refresh_token",
      refresh_token: refreshToken,
    },
    json: true,
  };

  const response = await fetch("https://accounts.spotify.com/api/token", {
    method: "POST",
    headers: {
      "Content-Type": "application/x-www-form-urlencoded",
      Authorization:
        "Basic " +
        new Buffer.from(client_id + ":" + client_secret).toString("base64"),
    },
    body: `grant_type=refresh_token&refresh_token=${refreshToken}`,
  });

  if (!response.ok) {
    return response.status;
  }
  const jsonResponse = await response.json();
  return jsonResponse.access_token;
}

export const addSong = async (token, playlistId, uris, playlistUrl) => {
  // Filter out undefined values and format URIs correctly
  const formattedUris = uris
    .filter((id) => id !== undefined)
    .map((id) => `spotify:track:${id}`);

  const addSong = await fetch(
    `https://api.spotify.com/v1/playlists/${playlistId}/tracks`,
    {
      method: "POST",
      headers: {
        Authorization: `Bearer ${token}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        uris: formattedUris,
      }),
    }
  );
  const addSongData = await addSong.json();
  if (addSongData.snapshot_id) {
    return playlistUrl;
  } else {
    console.error(addSongData);
    return;
  }
};

export const makePlaylist = async (token) => {
  const user = await fetch("https://api.spotify.com/v1/me", {
    headers: {
      Authorization: `Bearer ${token}`,
    },
  });
  const userData = await user.json();
  const userId = userData.id;
  const playlistName = "My MusiJournal Playlist";
  const playlistDescription = "A playlist created by MusiJournal.";
  const playlist = await fetch(
    `https://api.spotify.com/v1/users/${userId}/playlists`,
    {
      method: "POST",
      headers: {
        Authorization: `Bearer ${token}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        name: playlistName,
        description: playlistDescription,
        public: false,
      }),
    }
  );
  const playlistData = await playlist.json();
  const playlistId = playlistData.id;
  const playlistUrl = playlistData.external_urls.spotify;

  return { playlistId, playlistUrl };
};

